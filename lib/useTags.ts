const broadUseTags = new Set(["기획", "디자인", "개발", "기타", "제작", "AI", "도구", "Google", "OpenAI", "Adobe", "GitHub", "Next.js"]);

export function normalizeUseTags(...groups: string[][]) {
  const tags = groups
    .flat()
    .map((tag) => useTagAliases[tag] ?? tag)
    .filter((tag) => tag && !broadUseTags.has(tag));

  return Array.from(new Set(tags)).slice(0, 3);
}
